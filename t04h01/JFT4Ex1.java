public class JFT4Ex1 {

	public static void main(String[] args) {
		// Declaring and Assignments
		int numberA = 100;
		int numberB = 30;
		
		// Output
		System.out.println("Number 1: "+numberA + " Number 2: "+numberB);
		System.out.println("Adding two numbers: " + (numberA + numberB) );
		System.out.println("Subtracting two numbers: "+ (numberA - numberB));
		System.out.println("Multiplying two numbers: " + (numberA * numberB));
		System.out.println("Dividing one number by another: " + (numberA / numberB));
		System.out.println("Returning the remainder of two number: " + (numberA % numberB));
	}

}